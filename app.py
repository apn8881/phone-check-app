            cell = ws.cell(row=row_idx, column=col_idx)
            
            if col_idx == 1 and pd.notna(value) and value != '':
                phone_str = str(value).strip()
                if phone_str and phone_str != '‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•':
                    cell.value = phone_str
                    cell.number_format = '@'
                else:
                    cell.value = phone_str
